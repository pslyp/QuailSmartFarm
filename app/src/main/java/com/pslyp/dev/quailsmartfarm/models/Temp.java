package com.pslyp.dev.quailsmartfarm.models;

public class Temp {

    private int min;
    private int max;

    public Temp(int min, int max) {
        this.min = min;
        this.max = max;
    }

    public int getMin() {
        return min;
    }

    public int getMax() {
        return max;
    }

}
